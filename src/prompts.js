export function buildPromptContext(company, question, filings = [], financialsData = null) {
  const filingSummary = filings.length
    ? filings
        .map((filing) => `${filing.title} | ${filing.filingType} | ${filing.publishedAt || "unknown date"}`)
        .join("\n")
    : "尚未导入公告。";

  const financialsSummary = financialsData?.providerStatus === "ok"
    ? `财务数据来源：${financialsData.source}
收入增速：${financialsData.revenueGrowth !== null ? financialsData.revenueGrowth.toFixed(1) + '%' : '缺失'}
毛利率：${financialsData.grossMargin !== null ? financialsData.grossMargin.toFixed(1) + '%' : '缺失'}
经营利润率：${financialsData.operatingMargin !== null ? financialsData.operatingMargin.toFixed(1) + '%' : '缺失'}
净利润：${financialsData.netIncome !== null ? financialsData.netIncome : '缺失'}
利润增速：${financialsData.profitGrowth !== null ? financialsData.profitGrowth.toFixed(1) + '%' : '缺失'}
FCF：${financialsData.freeCashFlow !== null ? financialsData.freeCashFlow : '缺失'}
Forward PE：${financialsData.forwardPE !== null ? financialsData.forwardPE : '缺失'}
回购金额：${financialsData.repurchaseOfStock !== null ? financialsData.repurchaseOfStock : '缺失'}
ROE：${financialsData.returnOnEquity !== null ? financialsData.returnOnEquity : '缺失'}
资产负债率：${financialsData.debtToEquity !== null ? financialsData.debtToEquity : '缺失'}`
    : "财务数据：未接入";

  return `公司：${company.nameZh} (${company.ticker})
英文名：${company.nameEn}
板块：${company.sector}
行业：${company.industry}
用户问题：${question || "用户尚未提出明确问题"}
已导入材料：
${filingSummary}

${financialsSummary}`;
}
